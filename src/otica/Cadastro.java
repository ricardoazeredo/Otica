    private void txtValorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtValorFocusLost
        String sv = txtValor.getText(); //Pega o valor do campo
        String vsf = sv.replace("R$", "").replace(" ", "").replace(",", "."); //evita erros de digitações com relação a regra do tipo
        //Tratamento do campo em branco com mensagem.
        if("".equals(vsf)){
            JOptionPane.showMessageDialog(null,"<html>Aviso!<br/>Digite um valor númerico no campo <strong>Valor</strong>.</html>");
        }else{
            BigDecimal valor = new BigDecimal(vsf);
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR")); //formata o campo para a moeda(Currency) Real.
            String valorFormatado = nf.format(valor); //Formata o campo para os valores
            txtValor.setText(valorFormatado);
        }
    }//GEN-LAST:event_txtValorFocusLost

    private void txtSinalFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSinalFocusLost
        String sv = txtSinal.getText();
        String vsf = sv.replace("R$", "").replace(" ", "").replace(",", ".");
        if("".equals(vsf)){
            JOptionPane.showMessageDialog(null,"<html>Aviso!<br/>Digite um valor númerico no campo <strong>Sinal</strong>.</html>");
        }else{
            BigDecimal valor = new BigDecimal(vsf);
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
            String valorFormatado = nf.format(valor);
            txtSinal.setText(valorFormatado);
        }
        //Automatizando a resposta do campo Restante para mostrar se o produto já foi quitado ou quanto falta.
        String val = txtValor.getText();
        String vsf1 = val.replace("R$", "").replace(" ", "").replace(".", "").replace(",", "."); //retira o R$, em seguida o espaço, o ponto do milhar e troca a vírgula por ponto.(1.300,00 -> 1300.00)
        String sin = txtSinal.getText();
        String vsf2 = sin.replace("R$", "").replace(" ", "").replace(".", "").replace(",", ".");
        double v = Double.parseDouble(vsf1);
        double s = Double.parseDouble(vsf2);
        double result = v - s;
        if (result == 0) {
            txtRestante.setText("PAGO!");
        } else {
            String res = Double.toString(result);
            BigDecimal r = new BigDecimal(res);
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt","BR"));
            String resFormatado = nf.format(r);
            txtRestante.setText(resFormatado);
        }

    }//GEN-LAST:event_txtSinalFocusLost

